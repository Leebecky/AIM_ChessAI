var getPieceVal = function (piece , x, y) {

    if (piece === null)
    {
        return 0;
    }


    var getAbsoluteVal = function (piece , is_white, x, y)
    {
        if (piece.type === 'p') 
        {
            return 10 + (is_white ? pawn_white_eval[y][x] : pawn_black_eval[y][x]);
        }
        else if (piece.type === 'r')
        {
            return 50 + (is_white ? rook_white_eval[y][x] : rook_black_eval[y][x]);
        }
        else if (piece.type === 'n')
        {
            return 30 + knight_eval[y][x];
        }
        else if (piece.type === 'b')
        {
            return 30 + (is_white ? bishop_white_eval[y][x] : bishop_black_eval[y][x]);
        }
        else if (piece.type === 'q')
        {
            return 90 + queen_eval[y][x];
        }
        else if (piece.type === 'k')
        {
            return 900 + (is_white ? king_white_eval[y][x] : king_black_eval[y][x]);
        }
        throw "Piece Type Unknown: " + piece.type;    
    };
    
    var absolute_value = getAbsoluteVal(piece, piece.color === 'w', x, y);
    return piece.color === 'w' ? absolute_value : -absolute_value;
};