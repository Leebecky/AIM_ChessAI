//Evaluate board using piece-square tables in a 8x8 matrix
var evaluate_board = function(board)
{
    var total_evaluation = 0;
    for (var i = 0; i < 8; i++)
    {
        for (var j = 0; j < 8; j++)
        {
            total_evaluation = total_evaluation + getPieceVal
            (board[i][j], i, j); 
        }
    }
    return total_evaluation;
};

